# AND operator (&):
print(9 & 5)  # 1
print(40 & 41)  # 40

# OR operator (|):
print(9 | 5)  # 13
print(40 | 41)  # 41

# XOR operator (^):
print(9 ^ 5)  # 12
print(40 ^ 41)  # 1

# NOT operator (~):
print(~9)  # -10
print(~5)  # -6

# Left Shift Operator (<<):
print(9 << 1)  # 18
print(9 << 2)  # 36

# Right Shift Operator (>>):
print(9 >> 1)  # 4
print(-9 >> 2)  # -3
