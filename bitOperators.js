// AND operator (&):
console.log(9 & 5); // 1
console.log(40 & 41); // 40

// OR operator (|):
console.log(9 | 5); // 13
console.log(40 | 41); // 41

// XOR operator (^):
console.log(9 ^ 5); // 12
console.log(40 ^ 41); // 1

// NOT operator (~):
console.log(~9); // -10
console.log(~5); // -6

// Left Shift Operator (<<):
console.log(9 << 1); // 18
console.log(9 << 2); // 36

// Right Shift Operator (>>):
console.log(9 >> 1); // 4
console.log(-9 >> 2); // -3
